package rajbir;
class Swap
{
public static void main(String args[])
{
int x=10,y=5,temp;
System.out.println("before swapping");
System.out.println("values of x:"+x);
System.out.println("values of y:"+y);
temp=x;
x=y;
y=temp;
System.out.println("after swapping");
System.out.println("values of x:"+x);
System.out.println("values of y:"+y);
}
}
